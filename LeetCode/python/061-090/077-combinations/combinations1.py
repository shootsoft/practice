class Solution:
    # @return a list of lists of integers
    def combine(self, n, k):
        r = list()

        for i in range(1, n-k+2):

            c = range(i, k+i)
            #print c

            for i in (1, k):
                if c[k-1] >n :
                    break
                if len(r)>0:
                    t = r[-1]
                    if t != c:
                        r.append(c[:])
                else:
                    r.append(c[:])
                #print 'r', c

                j = k-1
                while j>0:
                    c[j] += 1
                    if c[j] > n :
                        break
                    elif j<k-1 and c[j] == c[j+1] :
                        c[j] -= 1
                        j -= 1
                        continue
                    elif c[j]<n:
                        r.append(c[:])
                        #print 'c', j,  c
                    else:
                        #print c
                        r.append(c[:])
                        j -= 1
                        #print 'f',j, i, c

                        if c[j+1] - c[j]>1:
                            c[j] += 1
                            for x in range(j+1, k):
                                c[x] = c[x-1]+1
                            r.append(c[:])
                            j = k -1
                            #print 'x', c
                            #break






        return r


